function seed(): void {
  const db = getDb();
  initSchema(db);

  let addedWiki = 0;
  let addedNews = 0;

  const insertWiki = db.prepare(`
    INSERT INTO wiki_entry (title, slug, category, summary, content, era, is_published, sort_order)
    VALUES (?, ?, ?, ?, ?, ?, 1, ?)
  `);
  const checkWiki = db.prepare('SELECT id FROM wiki_entry WHERE slug = ?');

  for (const entry of WIKI_ENTRIES) {
    const slug = slugify(entry.title);
    if (checkWiki.get(slug)) {
      console.log(`  [跳过] 百科已存在：${entry.title}`);
      continue;
    }
    insertWiki.run(
      entry.title,
      slug,
      entry.category,
      entry.summary ?? '',
      entry.content,
      entry.era ?? null,
      entry.sort_order ?? 0
    );
    addedWiki++;
    console.log(`  [新增] 百科：${entry.title}`);
  }

  const insertNews = db.prepare(`
    INSERT INTO news_article (title, slug, category, summary, content, source, event_date, is_published, is_pinned)
    VALUES (?, ?, ?, ?, ?, ?, ?, 1, ?)
  `);
  const checkNews = db.prepare('SELECT id FROM news_article WHERE slug = ?');

  for (const article of NEWS_ARTICLES) {
    const slug = slugify(article.title);
    if (checkNews.get(slug)) {
      console.log(`  [跳过] 新闻已存在：${article.title}`);
      continue;
    }
    insertNews.run(
      article.title,
      slug,
      article.category,
      article.summary ?? '',
      article.content,
      article.source ?? null,
      article.event_date ?? null,
      article.is_pinned ? 1 : 0
    );
    addedNews++;
    console.log(`  [新增] 新闻：${article.title}`);
  }

  console.log(`\n完成：新增 ${addedWiki} 篇百科词条，${addedNews} 篇新闻动态。`);
}

seed();
process.exit(0);
